#include "ft_printf.h"

int	ft_intlen(long int n)
{
	long int	nn;
	int			i;

	nn = n;
	if (n < 0)
	{
		i = 2;
		nn = -nn;
	}
	else
		i = 1;
	while (nn > 9)
	{
		nn = nn / 10;
		i++;
	}
	return (i);
}
